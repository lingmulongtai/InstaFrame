let currentLang = localStorage.getItem('instaframe_lang') || 'en';

function t(key) {
  return I18N[currentLang][key] || I18N['en'][key] || key;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('instaframe_lang', lang);
  applyTranslations();
}

function toggleLang() {
  setLang(currentLang === 'en' ? 'ja' : 'en');
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const attr = el.getAttribute('data-i18n-attr');
    if (attr) {
      el.setAttribute(attr, t(key));
    } else {
      el.textContent = t(key);
    }
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.getAttribute('data-i18n-html'));
  });
  // Update lang toggle button label
  const btn = document.getElementById('langToggleBtn');
  if (btn) btn.textContent = t('langToggle');

  // Set tooltip (title attr) on truncated setting labels so hover shows full text
  document.querySelectorAll('.setting-label[data-i18n]').forEach(el => {
    el.setAttribute('title', el.textContent);
  });
}
